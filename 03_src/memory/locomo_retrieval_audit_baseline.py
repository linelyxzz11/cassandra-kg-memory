"""Baseline consistency audit for strong hybrid run. Finds root cause of R@1/MRR gap.""" 
import csv, json, re, statistics, time
from collections import defaultdict
from pathlib import Path
import numpy as np

BASE = Path("D:/memorytable/cassandra-kg-memory/results")
OUT = Path("D:/memorytable/cassandra-kg-memory/reports/locomo_retrieval_hybrid_kg_attribution_audit")
OUT.mkdir(parents=True, exist_ok=True)
t0 = time.time()

print("=== Loading data ===")
memories = {}
with (BASE/"locomo_memory_records.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f): memories[r["memory_id"]] = r

qas = {}
with (BASE/"locomo_qa_records.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f): qas[r["qa_id"]] = r

gold_map = defaultdict(set)
with (BASE/"locomo_evidence_map.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f): gold_map[r["qa_id"]].add(r["memory_id"])

mem_ids = sorted(memories.keys())
cat14 = sorted([qid for qid, q in qas.items() if q["category"] != "5"])
mem_sample = {mid: memories[mid]["sample_id"] for mid in mem_ids}
qa_sample = {qid: qas[qid]["sample_id"] for qid in cat14}
dia2mid = {memories[mid]["dia_id"]: mid for mid in mem_ids if "dia_id" in memories[mid]}

# === A/B: Count audit ===
print("\n=== Count Audit ===")
cat_counts = defaultdict(int)
for qid in cat14: cat_counts[qas[qid]["category"]] += 1
truth_counts = [len(gold_map[qid]) for qid in cat14]
sample_counts = []
for qid in cat14:
    qs = qa_sample[qid]
    sample_counts.append(sum(1 for m in mem_ids if mem_sample[m] == qs))

ac_rows = [{"metric":k, "value":v} for k, v in {
    "n_questions_total": len(cat14),
    "n_cat1": cat_counts.get("1",0),
    "n_cat2": cat_counts.get("2",0),
    "n_cat3": cat_counts.get("3",0),
    "n_cat4": cat_counts.get("4",0),
    "avg_truth_per_question": round(statistics.mean(truth_counts),2),
    "median_truth_per_question": statistics.median(truth_counts),
    "n_memory_records": len(mem_ids),
    "n_memories_with_KG": len([m for m in mem_ids if m in dia2mid]),
    "avg_sample_scoped_memories": round(statistics.mean(sample_counts), 0),
    "min_sample_memories": min(sample_counts),
    "max_sample_memories": max(sample_counts),
}.items()]

with (OUT/"audit_counts.csv").open("w",newline="",encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["metric","value"])
    w.writeheader(); w.writerows(ac_rows)
for r in ac_rows: print(f"  {r['metric']:35s} = {r['value']}")

# === C: Old result source manifest ===
