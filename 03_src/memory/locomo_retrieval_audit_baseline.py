with (OUT/"old_result_source_manifest.md").open("w") as f: f.write(manifest)

# === D: Recompute using pre-computed CSV data (the canonical source) ===
print("\n=== Recomputing Baselines from canonical CSVs ===")

# Load pre-computed Dense top10
dense_precomp = {}
with (BASE/"locomo_dense_bge_results.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f):
        dense_precomp[r["qa_id"]] = {"hit1": int(r.get("hit1",0)), "hit5": int(r.get("hit5",0)), "hit10": int(r.get("hit10",0)), "rr": float(r.get("rr",0))}

# Load pre-computed BM25 top10  
bm25_precomp = {}
with (BASE/"locomo_bm25_results.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f):
        bm25_precomp[r["qa_id"]] = {"hit1": int(r.get("hit1",0)), "hit5": int(r.get("hit5",0)), "hit10": int(r.get("hit10",0)), "rr": float(r.get("rr",0))}

def from_precomp(cache, qids, label):
    rows = []
    for cat in sorted(set(qas[qid]["category"] for qid in qids)):
        cqids = [q for q in qids if qas[qid]["category"] == cat and q in cache]
        if not cqids: continue
        n = len(cqids)
        h1 = sum(cache.get(q,{}).get("hit1",0) for q in cqids)
        h10 = sum(cache.get(q,{}).get("hit10",0) for q in cqids)
        rrs = [cache.get(q,{}).get("rr",0) for q in cqids]
        mrr = statistics.mean(rrs) if rrs else 0
        rows.append({"method": label, "category": f"cat{cat}", "R@1": round(h1/n,4), "R@10": round(h10/n,4), "MRR": round(mrr,4), "n": n})
    valid_qids_all = [q for q in qids if q in cache]
    if valid_qids_all:
        all_n = len(valid_qids_all)
        all_h1 = sum(cache.get(q,{}).get("hit1",0) for q in valid_qids_all)
        all_h10 = sum(cache.get(q,{}).get("hit10",0) for q in valid_qids_all)
        all_rrs = [cache.get(q,{}).get("rr",0) for q in valid_qids_all]
        all_mrr = statistics.mean(all_rrs) if all_rrs else 0
        rows.append({"method": label, "category": "overall", "R@1": round(all_h1/all_n,4), "R@10": round(all_h10/all_n,4), "MRR": round(all_mrr,4), "n": all_n})
    return rows

dens_cat = from_precomp(dense_precomp, cat14, "Dense-bge_canonical")
bm25_cat = from_precomp(bm25_precomp, cat14, "BM25_canonical")
canonical = dens_cat + bm25_cat

cf = ["method","category","R@1","R@10","MRR","n"]
with (OUT/"recomputed_baselines_by_category.csv").open("w",newline="",encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=cf, extrasaction="ignore")
    w.writeheader(); w.writerows(canonical)

# Now load from analysis_category_wise.csv (old expected)
old_expected = {}
with (BASE/"sample_scoped/analysis_category_wise.csv").open(encoding="utf-8-sig") as f:
    for r in csv.DictReader(f):
        key = (r["method"].split("_")[0] if "_" in r["method"] else r["method"], r["category"])
        old_expected[key] = r

# Diff table
diff_rows = []
name_map = {"Dense-bge_canonical": "Dense-bge", "BM25_canonical": "BM25"}
for cr in canonical:
    method = name_map.get(cr["method"], cr["method"])
    cat = cr["category"]
    key = (method, cat)
    old = old_expected.get(key)
    if old:
        for metric in ["R@1","R@10","MRR"]:
            new_val = cr[metric]
            old_val = float(old[metric])
            delta = new_val - old_val
            diff_rows.append({
                "method": method, "category": cat, "metric": metric,
                "old_value": round(old_val,4), "new_value": round(new_val,4),
                "delta": round(delta,4), "relative_delta": round(delta/abs(old_val)*100,1) if old_val else 0,
                "possible_reason": "exact match from canonical CSV" if abs(delta) < 0.001 else "cross-sample vs sample-scoped",
            })

with (OUT/"baseline_diff.csv").open("w",newline="",encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["method","category","metric","old_value","new_value","delta","relative_delta","possible_reason"])
    w.writeheader(); w.writerows(diff_rows)

print(f"  Diff rows: {len(diff_rows)}")
mismatches = [d for d in diff_rows if abs(d["delta"]) > 0.01]
if mismatches:
    print(f"  MISMATCHES (delta > 0.01): {len(mismatches)}")
    for d in mismatches[:10]:
        print(f"    {d['method']} {d['category']} {d['metric']}: old={d['old_value']:.4f} new={d['new_value']:.4f} delta={d['delta']:+.4f}")

# Also output overall baseline comparison
ov_simple = [{k:v for k,v in cr.items() if k in ["method","R@1","R@10","MRR","n"]} for cr in canonical if cr["category"]=="overall"]
with (OUT/"recomputed_baselines_overall.csv").open("w",newline="",encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["method","R@1","R@10","MRR","n"])
    w.writeheader(); w.writerows(ov_simple)

for r in ov_simple:
    print(f"  Canonical {r['method']:25s} R@1={r['R@1']:.4f} R@10={r['R@10']:.4f} MRR={r['MRR']:.4f}")

# === E: KG prior leakage check ===
print("\n=== KG Leakage Check ===")
kg_edges_raw = []
with (BASE/"locomo_kg_edges_spacy.csv").open(encoding="utf-8-sig") as f:
    for e in csv.DictReader(f):
        ev, gid = e.get("evidence",""), e["graph_id"]
        for mid, m in memories.items():
            if m["sample_id"] == gid and (m["dia_id"] == ev or ev in mid):
                kg_edges_raw.append(e)
                break
has_kg = {mid: 1.0 for mid in memories if any(
    e.get("evidence") and (memories[mid]["dia_id"] == e.get("evidence") or e.get("evidence") in mid)
    for e in kg_edges_raw if e.get("graph_id") == memories[mid]["sample_id"])}

lk_rows = []
for cat in sorted(set(qas[qid]["category"] for qid in cat14)):
    cqids = [q for q in cat14 if qas[q]["category"] == cat]
    truth_kg = 0; truth_tot = 0
    nontruth_kg = 0; nontruth_tot = 0
    for qid in cqids:
        gold = gold_map[qid]
        for mid in gold:
            truth_tot += 1
            if has_kg.get(mid): truth_kg += 1
        qs = qa_sample[qid]
        for mid in mem_ids:
            if mem_sample[mid] == qs and mid not in gold:
                nontruth_tot += 1
                if has_kg.get(mid): nontruth_kg += 1
    truth_rate = truth_kg/truth_tot if truth_tot else 0
    nontruth_rate = nontruth_kg/nontruth_tot if nontruth_tot else 0
    odds = (truth_rate/(1-truth_rate)) / (nontruth_rate/(1-nontruth_rate)) if nontruth_rate < 1 and truth_rate < 1 else 99
    lk_rows.append({
        "category": f"cat{cat}", "truth_hasKG_rate": round(truth_rate,4),
        "nontruth_hasKG_rate": round(nontruth_rate,4),
        "odds_ratio": round(odds,2),
        "notes": "KG edge evidence is from conversation text, NOT answer labels. KG extraction happened before retrieval experiments." if cat == "1" else ""
    })
with (OUT/"kg_prior_leakage_check.csv").open("w",newline="",encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["category","truth_hasKG_rate","nontruth_hasKG_rate","odds_ratio","notes"])
    w.writeheader(); w.writerows(lk_rows)
for r in lk_rows:
    print(f"  {r['category']}: truth_kg={r['truth_hasKG_rate']:.2%} nontruth_kg={r['nontruth_hasKG_rate']:.2%} odds_ratio={r['odds_ratio']}")

